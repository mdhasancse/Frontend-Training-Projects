

// If the ‘characters’ passed is the last character of the string, the function returns true, else false.

var str = "Hello Cybrosys";
var is_last = str.endsWith('sys');
console.log(is_last); // The console gives the value true as the string ends with the character ‘sys’.



var str = "Hello Cybrosys";
var is_last = str.endsWith('C');
console.log(is_last); // The console gives a false value as the string does not end with the character ‘C’.



